# Приклад
def fib(n): 
    print("Послідовність чисел Фібоначі, які не перевищують", n)
    a, b = 0, 1 # мабуть, те саме, що a=0 b=1
    while b < n:
        print(b)
        a, b = b, a + b # не розумію цю стрічку, але явно не те саме, що a=b b=a+b
fib(100)

